void cycle_selection(uint8_t * var, uint8_t len, bool inc) {
  uint8_t val = *var;
  if (inc) {
    if (val == (len - 1)) {
      *(var) = 0;
    } else {
      *(var) = (val + 1);
    }
  } else {
    if (val == 0) {
      *(var) = (len - 1);
    } else {
      *(var) = (val - 1);
    }
  }
}
